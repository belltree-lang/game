export class Bag {
  constructor() {
    this.queue = [];
    this.refill();
  }

  refill() {
    const types = Object.keys(SHAPES);
    for (let i = types.length - 1; i > 0; i -= 1) {
      const j = Math.floor(Math.random() * (i + 1));
      [types[i], types[j]] = [types[j], types[i]];
    }
    this.queue.push(...types);
  }

  draw() {
    if (this.queue.length === 0) {
      this.refill();
    }
    return this.queue.pop();
  }
}
